import string

print(string.ascii_lowercase)  # маленькие буквы английского алфавита
print(string.ascii_uppercase)  # большие буквы английского алфавита
print(string.ascii_letters)  # все буквы английского алфавита
print(string.punctuation)  # спец.символы
print(string.printable)  # вообще вся клавиатура
print(string.digits)  # цифры десятичная система
print(string.octdigits)  # b=8
print(string.hexdigits)  # b=16



